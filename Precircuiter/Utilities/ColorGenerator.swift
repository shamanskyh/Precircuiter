    func toGelColor() -> CGColor? {
        
        var whitespaceTrimmed = self.replacingOccurrences(of: " ", with: "").lowercased()
        
        if (whitespaceTrimmed == "N/C".lowercased() || whitespaceTrimmed == "NC".lowercased()) {
            return NSColor(red: (230.0 / 255.0), green: (230.0 / 255.0), blue: (230.0 / 255.0), alpha: 1.0).cgColor
        }
        
        // ignore any frost
        if let plusSign = self.range(of: "+") {
            whitespaceTrimmed = whitespaceTrimmed.substring(to: plusSign.lowerBound)
        }
        
        // determine the manufacturer
        var manufacturer: GelManufacturer?
        var convertToInt = true
        if whitespaceTrimmed.range(of: "roscolene") != nil {
            manufacturer = .roscolene
        } else if whitespaceTrimmed.range(of: "rosco") != nil {
            manufacturer = .roscolux
        } else if whitespaceTrimmed.range(of: "lee") != nil {
            manufacturer = .lee
        } else if whitespaceTrimmed.range(of: "gam") != nil {
            manufacturer = .gam
        } else if whitespaceTrimmed.range(of: "apollo") != nil {
            manufacturer = .apollo
        } else if whitespaceTrimmed.range(of: "e-colour") != nil ||
                  whitespaceTrimmed.range(of: "ecolour") != nil ||
                  whitespaceTrimmed.range(of: "e-color") != nil ||
                  whitespaceTrimmed.range(of: "ecolor") != nil {
            manufacturer = .eColour
        } else if whitespaceTrimmed.range(of: "super") != nil {
            manufacturer = .supergel
        } else if whitespaceTrimmed.range(of: "r") != nil {
            manufacturer = .roscolux
        } else if whitespaceTrimmed.range(of: "cl") != nil{
            manufacturer = .lee
            convertToInt = false    // don't convert the weird lee gel
        } else if whitespaceTrimmed.range(of: "l") != nil{
            manufacturer = .lee
        } else if whitespaceTrimmed.range(of: "g") != nil {
            manufacturer = .gam
        } else if whitespaceTrimmed.range(of: "s") != nil {
            manufacturer = .supergel
        } else if whitespaceTrimmed.range(of: "a") != nil {
            manufacturer = .apollo
        }
        
        // try to remove leading zeroes, unless it's the weird lee gel
        if convertToInt {
            var str = ""
            for c in whitespaceTrimmed.unicodeScalars {
                if c.value >= 48 && c.value <= 57 { // Unicode digits 0-9
                    str.append(String(c))
                }
            }
            if let intVal = Int(str) {
                whitespaceTrimmed = "\(intVal)"
            } else {
                whitespaceTrimmed = str
            }
        }

        guard let m = manufacturer else {
            return nil
        }
        
        switch (m) {
        case .roscolux: return roscoluxGelFromCode(whitespaceTrimmed)
        case .lee: return leeGelFromCode(whitespaceTrimmed)
        case .gam: return gamGelFromCode(whitespaceTrimmed)
        case .apollo: return apolloGelFromCode(whitespaceTrimmed)
        case .supergel: return supergelGelFromCode(whitespaceTrimmed)
        case .eColour: return eColourGelFromCode(whitespaceTrimmed)
        case .roscolene: return roscoleneGelFromCode(whitespaceTrimmed)
        }
        
    }
    
