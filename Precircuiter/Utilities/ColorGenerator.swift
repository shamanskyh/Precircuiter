    func toGelColor() -> CGColor? {
        
        var whitespaceTrimmed = self.stringByReplacingOccurrencesOfString(" ", withString: "").lowercaseString
        
        if (whitespaceTrimmed == "N/C".lowercaseString || whitespaceTrimmed == "NC".lowercaseString) {
            return NSColor(red: (230.0 / 255.0), green: (230.0 / 255.0), blue: (230.0 / 255.0), alpha: 1.0).CGColor
        }
        
        // ignore any frost
        if let plusSign = self.rangeOfString("+") {
            whitespaceTrimmed = whitespaceTrimmed.substringToIndex(plusSign.startIndex)
        }
        
        // determine the manufacturer
        var manufacturer: GelManufacturer?
        var convertToInt = true
        if whitespaceTrimmed.rangeOfString("roscolene") != nil {
            manufacturer = .Roscolene
        } else if whitespaceTrimmed.rangeOfString("rosco") != nil {
            manufacturer = .Roscolux
        } else if whitespaceTrimmed.rangeOfString("lee") != nil {
            manufacturer = .Lee
        } else if whitespaceTrimmed.rangeOfString("gam") != nil {
            manufacturer = .GAM
        } else if whitespaceTrimmed.rangeOfString("apollo") != nil {
            manufacturer = .Apollo
        } else if whitespaceTrimmed.rangeOfString("e-colour") != nil ||
                  whitespaceTrimmed.rangeOfString("ecolour") != nil ||
                  whitespaceTrimmed.rangeOfString("e-color") != nil ||
                  whitespaceTrimmed.rangeOfString("ecolor") != nil {
            manufacturer = .EColour
        } else if whitespaceTrimmed.rangeOfString("super") != nil {
            manufacturer = .Supergel
        } else if whitespaceTrimmed.rangeOfString("r") != nil {
            manufacturer = .Roscolux
        } else if whitespaceTrimmed.rangeOfString("cl") != nil{
            manufacturer = .Lee
            convertToInt = false    // don't convert the weird lee gel
        } else if whitespaceTrimmed.rangeOfString("l") != nil{
            manufacturer = .Lee
        } else if whitespaceTrimmed.rangeOfString("g") != nil {
            manufacturer = .GAM
        } else if whitespaceTrimmed.rangeOfString("s") != nil {
            manufacturer = .Supergel
        } else if whitespaceTrimmed.rangeOfString("a") != nil {
            manufacturer = .Apollo
        }
        
        // try to remove leading zeroes, unless it's the weird lee gel
        if convertToInt {
            var str = ""
            for c in whitespaceTrimmed.unicodeScalars {
                if c.value >= 48 && c.value <= 57 { // Unicode digits 0-9
                    str.append(c)
                }
            }
            if let intVal = Int(str) {
                whitespaceTrimmed = "\(intVal)"
            } else {
                whitespaceTrimmed = str
            }
        }

        guard let m = manufacturer else {
            return nil
        }
        
        switch (m) {
        case .Roscolux: return roscoluxGelFromCode(whitespaceTrimmed)
        case .Lee: return leeGelFromCode(whitespaceTrimmed)
        case .GAM: return gamGelFromCode(whitespaceTrimmed)
        case .Apollo: return apolloGelFromCode(whitespaceTrimmed)
        case .Supergel: return supergelGelFromCode(whitespaceTrimmed)
        case .EColour: return eColourGelFromCode(whitespaceTrimmed)
        case .Roscolene: return roscoleneGelFromCode(whitespaceTrimmed)
        }
        
    }
    
